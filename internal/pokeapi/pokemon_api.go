type PokemonNames struct {
	Count    int    `json:"count"`
	Next     *string `json:"next"`
	Previous *string    `json:"previous"`
	Results  []struct {
		Name string `json:"name"`
		URL  string `json:"url"`
	} `json:"results"`
}

func (c *client) GetPokemonNamesBatch(pn PokemonNames) (PokemonNames, error) {
	var data io.ReadCloser
	var fullURL string

	if pn.Next != nil {
		fullURL = *pn.Next
	} else {
		fullURL = c.baseURL.String() + "/pokemon/"
	}

	val, ok := c.cache.Get(fullURL)

	if ok {
		data = io.NopCloser(bytes.NewReader(val))
	} else {
		req, err := c.newRequest("GET", fullURL, nil)
		if err != nil {
			return PokemonNames{}, err
		}

		res, err := c.httpClient.Do(req)
		if err != nil {
			return PokemonNames{}, err
		}

		defer res.Body.Close()

		data = res.Body
	}

	var name PokemonNames
	decoder := json.NewDecoder(data)
	if err := decoder.Decode(&name); err != nil {
		return PokemonNames{}, err
	}

	return name, nil
}

func (c *client) GetTotalPokemonBaseExperience() (int, error) {
	var sum int
	var names []string

	batch, err := c.GetPokemonNamesBatch(PokemonNames{})
	if err != nil {
		return 0, err
	}

	for ;batch.Next != nil; {
		for _, result := range batch.Results {
			names = append(names, result.Name)	
		}

		newBatch, err := c.GetPokemonNamesBatch(batch)
		if err != nil {
			return 0, nil
		}

		batch = newBatch
	}
	
	for i, name := range names {
		fmt.Println(fmt.Sprintf("Getting pokemon %d/%d: %s", i, len(names), name))
		pokemon, err := c.GetPokemonInformation(name)
		if err != nil {
			return 0, err
		}

		sum += pokemon.BaseExperience
	}

	return sum, nil
}

